            </p>
            <p>
              <strong>
                ──『しあわせ鳥見んぐ』以外にどんなタイトル案がありましたか？
              </strong>
            </p>
            <p>
              <span class="interviewee _red">
                きなこ
              </span>
              ：
              <strong>
                『鳥見びより』
              </strong>
              に、
              <strong>
                『アーバンバーダー』
              </strong>
              に、
              <strong>
                『ハッピーバードデー』
              </strong>
              ……。とりあえず、バードウォッチングの漫画だと一目でわかるようなタイトルにしたいと思っていました。
            </p>
            <p>
              今のタイトルも最初は『鳥見んぐ』だけだったんですけど、
              <strong>
                「しあわせ」
              </strong>
              をつけることで柔らかい雰囲気になったかなと。
              <strong>
                <span class="underline">
                  ストーリーの根本に、童話の「幸せの青い鳥」を意識している
                </span>
              </strong>
              部分がありますし。
            </p>
            <p class="text-center">
              <img permalink="" data-src="https://media.comicspace.jp/wp-content/uploads/2021/10/toriming007.jpg" alt=""
                width="350" height="940" class="alignnone size-full wp-image-19003"
                src="https://media.comicspace.jp/wp-content/uploads/2021/10/toriming007.jpg">
              <br>
              <span class="caption">
                探していたものはすぐ近くに
              </span>
            </p>
            <p>
              <strong>
                ──野暮な質問ですが、「鳥見」と「トリミング」をかけているということでいいんでしょうか。
              </strong>
            </p>
            <p>
              <span class="interviewee _red">
                きなこ
              </span>
              ：はい。バードウォッチングの俗称としての「鳥見」に「ing（～をすること）」をつけて「鳥見んぐ」と、
              <span class="highlight">
                <strong>
                  絵や写真で鳥の姿を切り取る
                </strong>
              </span>
              という意味での「トリミング」をかけています。
            </p>
            <p>
              <span class="interviewee _green">
                担当編集
              </span>
              ：きなこ先生、そういう言葉選びがすごく上手ですよね。作中にもよくダジャレが出てきますし。
            </p>
            <p>
              <span class="interviewee _red">
                きなこ
              </span>
              ：1話に最低1回は鳥に絡めたダジャレを入れないと落ち着かないというか……。ギャグは控えめにと言われてたはずなんですけど、やっぱり『PJK』のノリが抜けていないのかも（笑）。
