            <h2>
              定期購読していた『BIRDER』とまさかのコラボ
            </h2>
            <p>
              <strong>
                ──本日はお忙しい中ありがとうございます。この取材を申し込んだ時点では『BIRDER』でもインタビューを受けられていたことを知らなかったので、告知を見たときびっくりしました。
              </strong>
            </p>
            <p>
              <span class="interviewee _red">
                わらびもちきなこ先生（以下、きなこ）
              </span>
              ：私も事前にまったく知らされていなくて、担当さんから突然「コラボ決まったよ！」と言われてびっくりしました（笑）。
              <strong>
                <span class="underline">
                  『BIRDER』は、この漫画を描き始めたころから資料も兼ねて定期購読していた雑誌だった
                </span>
              </strong>
              ので。
            </p>
            <p class="text-center">
              <a class="imageAnchor" href="https://media.comicspace.jp/archives/18988/toriming002">
                <img permalink="https://media.comicspace.jp/archives/18988/toriming002"
                  data-src="https://media.comicspace.jp/wp-content/uploads/2021/10/toriming002.jpg" alt="" width="400"
                  height="564" class="alignnone size-full wp-image-18998"
                  src="https://media.comicspace.jp/wp-content/uploads/2021/10/toriming002.jpg">
              </a>
              <br>
              <span class="caption">
                『BIRDER』2021年11月号
              </span>
            </p>
            <p>
              ただ、『BIRDER』のインタビューでは現実のバードウォッチングに関連した話が多かったので、今日はまた違ったお話もできるかなと思っています。
            </p>
            <p>
              <strong>
                ──インタビューだけでなく、雑誌の表紙も描かれたり、先生も記事を寄稿されたりとかなり大がかりなコラボ企画でしたが、どういった経緯で実現したんでしょうか。
              </strong>
            </p>
            <p>
              <span class="interviewee _green">
                担当編集
              </span>
              ：『しあわせ鳥見んぐ』はきららでも今イチオシの作品なので、単行本の発売にあわせてこれまでにない施策がしたいなと考えていました。そんなとき、私も作品資料として読んでいた『BIRDER』に、たまたま『
              <a href="https://comicspace.jp/title/122732" rel="noopener noreferrer" target="_blank">
                秋山さんのとりライフ
              </a>
              』というバードウォッチング漫画のインタビュー記事が載っていたんです。
            </p>
            <p>
              それで、「ウチにも『しあわせ鳥見んぐ』って漫画があるんですけれども……」と恐る恐るオファーをしてみたら興味を持っていただけて。打ち合わせを重ねるうちにどんどん話が膨らんでいき、最終的に表紙も描かせてもらうことになりました。
            </p>
            <p>
              <strong>
                ──バードウォッチングの漫画を描こうと思ったのはどうしてですか？
              </strong>
            </p>
            <p>
              <span class="interviewee _red">
                きなこ
              </span>
              ：前作『
              <a href="https://comicspace.jp/title/116439" rel="noopener noreferrer" target="_blank">
                佐藤さんはPJK
              </a>
              』の終了が決まって次回作の構想を練っていたのが、ちょうど私がバードウォッチングを始めたころだったんです。
              <span class="highlight">
                <strong>
                  この楽しさを漫画にしたら面白そうだな
                </strong>
              </span>
              と思ったのが最初のきっかけですね。
            </p>
            <span class="comicspace-title-parameter" title-id="116439">
            </span>
            <p>
              <strong>
                ──『佐藤さんはPJK』はハイテンションなギャグ4コマでしたが、今作はストーリー重視で落ち着いた雰囲気だなと感じます。
              </strong>
            </p>
            <p>
              <span class="interviewee _red">
                きなこ
              </span>
              ：実は、前の担当さんからも
              <strong>
                「次はギャグじゃない方向にしてみては」
              </strong>
              と言われていました。理由はちゃんと聞いてないんですけど、前作の反応とか私の絵柄とかを見て、ギャグ以外の可能性を試そうとしてくださったのかもしれません。
            </p>
            <p>
              <strong>
                ──前の担当さんからそう言われたときの気持ちはいかがでしたか？
              </strong>
            </p>
            <p>
              <span class="interviewee _red">
                きなこ
              </span>
              ：『PJK』は私が初めてまともに描いた漫画だったので、
              <span class="bgline">
                <strong>
                  ギャグを封印されたら何を描けばいいんだ……
                </strong>
              </span>
              って正直なところ思いました（笑）。
            </p>
            <p>
              なので、何か主軸になるようなテーマがほしいなと思って。そこで自分がちょうどはまっていたバードウォッチングに白羽の矢が立った感じです。
            </p>
            <p class="text-center">
              <img permalink="" data-src="https://media.comicspace.jp/wp-content/uploads/2021/10/toriming003.jpg" alt=""
