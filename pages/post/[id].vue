            <h2>
              「鳥見をする人」と「鳥の姿を切り取る」をかけたタイトル
            </h2>
            <p>
              <strong>
                ──きらら作品には女子高生のキャラクターが多いと思いますが、本作ですずたちを大学生にしたのはなぜですか？
              </strong>
            </p>
            <p>
              <span class="interviewee _red">
                きなこ
              </span>
              ：バードウォッチングを取り扱うにしても、鳥を見る──インプットするだけじゃなく、
              <span class="highlight">
                <strong>
                  絵を描いたり写真を撮ったりというアウトプットの要素を入れたかった
                </strong>
              </span>
              んです。そうなると、高校生よりも大学生、それも専門的に学んでいる芸大生のほうが説得力があるかなって。
            </p>
            <p class="text-center">
              <img permalink="" data-src="https://media.comicspace.jp/wp-content/uploads/2021/10/toriming005.jpg" alt=""
                width="500" height="376" class="alignnone size-full wp-image-19001"
                src="https://media.comicspace.jp/wp-content/uploads/2021/10/toriming005.jpg">
              <br>
              <span class="caption">
                「絵に個性がない」と言われ悩んでいたすずだが、鳥と出会い自信を取り戻す
              </span>
            </p>
            <p>
              あと、単純に大学生のほうが時間の面でもお金の面でも自由度が高いから、漫画を描くにあたって動かしやすそうというのも理由のひとつです。
            </p>
            <p>
              <strong>
                ──伊豆沼の回も、顧問の先生に引率されてではなく、自分たちで車を運転して現地まで行っていたのが大学生っぽかったです。
