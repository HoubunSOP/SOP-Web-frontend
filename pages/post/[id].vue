              <a href="https://birder.jp/" rel="noopener noreferrer" target="_blank">
                BIRDER
              </a>
              』でも特集が組まれるなど、漫画好きだけでなくバードウォッチャーからも注目を集めている『
              <a href="https://comicspace.jp/title/127620" rel="noopener noreferrer" target="_blank">
