              <strong>
                探鳥会
              </strong>
              というイベントがあることを知り、鳥を見るくらいなら自分にもできるかなと軽い気持ちで参加してみたら、バードウォッチングの魅力に取りつかれてしまいました。
            </p>
            <p>
              <strong>
                ──『しあわせ鳥見んぐ』の舞台が山形なのも、先生が山形在住だからなんですね。
              </strong>
