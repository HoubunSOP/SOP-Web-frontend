                </strong>
              </span>
              、みたいなギャップが逆に面白いかなあ、と……。すみません、そんなに深い意味はありません（笑）。
            </p>
            <p class="text-center">
              <img permalink="" data-src="https://media.comicspace.jp/wp-content/uploads/2021/10/toriming006.jpg" alt=""
