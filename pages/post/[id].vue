              </span>
            </p>
            <p>
              <strong>
                ──前の担当さんからの助言と、バードウォッチングという題材がうまくマッチしたんですね。バードウォッチングを始めたきっかけは何だったんでしょうか。
              </strong>
            </p>
            <p>
              <span class="interviewee _red">
                きなこ
              </span>
              ：数年前に山形に引っ越しをして、
              <strong>
                <span class="underline">
